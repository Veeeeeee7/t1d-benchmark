_CSV_COLS = ["Name", *THETA_NAMES, "BW", "Ib", "CR_true", "dose_mult"]


def write_phase0_csv(subjects, path: str) -> str:
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    with open(path, "w") as fh:
        fh.write(",".join(_CSV_COLS) + "\n")
        for s in subjects:
            vals = [s.name, *[f"{v:.8g}" for v in s.theta],
                    f"{s.BW:.6g}", f"{s.Ib:.6g}", f"{s.CR_true:.6g}", f"{s.dose_mult:.6g}"]
            fh.write(",".join(str(v) for v in vals) + "\n")
    return path


def load_phase0_csv(path: str):
    import csv
    subs = []
    with open(path, newline="") as fh:
        for row in csv.DictReader(fh):
            theta = np.array([float(row[n]) for n in THETA_NAMES], dtype=float)
            subs.append(Phase0Subject(
                name=row["Name"], theta=theta, BW=float(row["BW"]),
                Ib=float(row["Ib"]), CR_true=float(row["CR_true"]),
                dose_mult=float(row["dose_mult"])))
    return subs


# ---------------------------------------------------------------------------
# Patient-matched cohort: build Phase0Subjects from an *augmented* patients.csv
# (the simglucose cohort with rbg_* columns appended by derive_replaybg_params).
# ---------------------------------------------------------------------------
RBG_THETA_COLS = [f"rbg_{n}" for n in THETA_NAMES]


def subjects_from_patients_csv(path: str):
    """Build Phase 0 subjects from the cached ReplayBG fit in ``patients.csv``.

    Each subject's true physiology is the best-fit ReplayBG ``theta`` for the
    corresponding simglucose patient (``rbg_*`` columns), run at that patient's
    ``BW`` / basal (``rbg_Ib``) / matched carb ratio (``rbg_CR_true``), with the
    patient's existing carb error (``dose_mult``). So Phase 0 patient *i* is the
    matched-model counterpart of Phase 2 patient *i*. Requires the columns
    written by ``derive_replaybg_params``.
    """
    import csv
    subs = []
    with open(path, newline="") as fh:
        reader = csv.DictReader(fh)
        if not all(c in (reader.fieldnames or []) for c in RBG_THETA_COLS):
            raise KeyError(
                f"{path} has no rbg_* columns; run "
                f"`python -m experiments.derive_replaybg_params --patients {path}` first")
        for row in reader:
            if row.get("rbg_SI") in (None, "", "nan"):
                continue   # patient not yet fit; skip
            theta = np.array([float(row[c]) for c in RBG_THETA_COLS], dtype=float)
            subs.append(Phase0Subject(
                name=row["Name"], theta=theta, BW=float(row["BW"]),
                Ib=float(row["rbg_Ib"]), CR_true=float(row["rbg_CR_true"]),
                dose_mult=float(row.get("dose_mult", 1.0) or 1.0)))
    return subs


def load_phase0_cohort(path: str):
    """Load a Phase 0 cohort, auto-detecting the source.

    * an augmented ``patients.csv`` (has ``rbg_*`` columns)  -> patient-matched
      cohort (`subjects_from_patients_csv`);
    * a native phase0 CSV (has bare ``ka2`` ... columns)     -> independent
      prior-draw cohort (`load_phase0_csv`).
    """
    import csv
    with open(path, newline="") as fh:
        header = next(csv.reader(fh), [])
    if "rbg_SI" in header:
        return subjects_from_patients_csv(path)
    if "SI" in header:
        return load_phase0_csv(path)
    raise KeyError(
        f"{path} is neither an augmented patients.csv (rbg_* columns) nor a "
        f"native phase0 cohort (ka2.. columns). Run derive_replaybg_params or "
        f"generate_phase0_patients first.")