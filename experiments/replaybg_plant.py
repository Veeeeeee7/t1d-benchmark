"""Phase 0 — the *self-consistent* ReplayBG plant.

Phase 2 uses simglucose / UVA-Padova as the ground-truth plant and a ReplayBG
ODE as the twin, so its numbers fold in an (intended) plant<->twin model
mismatch. Phase 0 removes that mismatch entirely: the plant **is** the ReplayBG
forward model. Both the identification CGM and the candidate-grid ground truth
are generated by ``replaybg_model.simulate`` at a known "true" theta, and the
twins (MCMC / SBI) fit that same model class. This is the best-case baseline:
"how well do these methods rank therapies when the model is correctly
specified?".

Design choices that keep Phase 0 comparable to Phase 2
------------------------------------------------------
* Same cadence/horizon: 3-min CGM, 1-min ODE grid, 24 h, 3 meals/day.
* Same meal schedule builder (copied here so this module never imports
  simglucose -- Phase 0 has no business depending on it).
* Same candidate grid and the same carb-counting-error mechanism: each patient's
  programmed carb ratio is mis-set by ``dose_mult = m``, so the baseline
  ``bolus_x1.00`` is sub-optimal and the optimal corrective factor is f* = m,
  which the production grid brackets on both sides -> interior optima.
* Same scoring object: these runs quack like ``simglucose_adapter.RunResult``
  (``cgm() / bg() / replaybg_inputs(dt) / sample_time``), so ``identify_*`` and
  ``evaluate_twin`` are reused **unchanged**.

The one deliberate difference from Phase 2 (besides the plant) is the sensor
noise: Phase 0 adds Gaussian N(0, sigma) noise that matches the MCMC likelihood
and the SBI simulator (both sigma=10). This removes the sensor-model mismatch
too, so Phase 0 is a clean "everything matches" lower bound on difficulty. Swap
``_add_noise`` for ``t1d_twin.sensor.add_cgm_noise`` if you want Dexcom realism.
"""
