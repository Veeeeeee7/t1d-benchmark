# ---------------------------------------------------------------------------
# Meal schedule  (copied verbatim from exp_common.meal_plan so Phase 0 stays
# simglucose-free; keep in sync if the Phase 2 schedule ever changes)
# ---------------------------------------------------------------------------
def meal_plan(hours: float, seed: int = SEED):
    """Deterministic 3-meals-a-day schedule as ``(hour, grams)`` tuples."""
    rng = np.random.default_rng(seed)
    anchors = [(7.0, 50.0), (12.5, 70.0), (19.0, 80.0)]
    meals = []
    n_days = int(np.ceil(hours / 24.0))
    for d in range(n_days):
        for h0, g0 in anchors:
            h = d * 24.0 + h0 + float(rng.uniform(-0.5, 0.5))
            g = float(g0 * rng.uniform(0.85, 1.15))
            if 0.0 < h < hours:
                meals.append((round(h, 3), round(g, 1)))
    return sorted(meals)


# ---------------------------------------------------------------------------
# A RunResult-compatible record (duck-typed; no simglucose import)
# ---------------------------------------------------------------------------
@dataclass
class RBGRun:
    """Minimal stand-in for ``simglucose_adapter.RunResult``.

    Exposes exactly the surface that ``identify_*`` and ``evaluate_twin`` use:
    ``cgm()``, ``bg()``, ``replaybg_inputs(dt)``, ``sample_time`` (+ ``BW`` /
    ``sensor_name`` / ``meta`` for parity).
    """
    _cgm: np.ndarray            # (n,) noisy CGM at sample times [mg/dL]
    _bg: np.ndarray             # (n,) noise-free IG at sample times [mg/dL]
    _insulin_sample: np.ndarray  # (n,) insulin [mU/kg/min] at sample cadence
    _cho_sample: np.ndarray      # (n,) CHO [mg/kg/min] at sample cadence
    sample_time: float
    BW: float
    sensor_name: str = SENSOR
    meta: dict = field(default_factory=dict)

    def cgm(self) -> np.ndarray:
        return self._cgm

    def bg(self) -> np.ndarray:
        return self._bg

    def replaybg_inputs(self, dt: float = 1.0):
        reps = self.sample_time / dt
        if abs(reps - round(reps)) > 1e-9:
            raise ValueError(
                f"sample_time ({self.sample_time}) must be an integer multiple of dt ({dt})")
        reps = int(round(reps))
        insulin = np.repeat(self._insulin_sample, reps)
        cho = np.repeat(self._cho_sample, reps)
        t_grid = np.arange(len(insulin)) * dt
        return t_grid, insulin, cho


# ---------------------------------------------------------------------------
# Phase 0 subject = a true ReplayBG theta + dosing constants
# ---------------------------------------------------------------------------
@dataclass
class Phase0Subject:
    name: str
    theta: np.ndarray          # (8,) true ReplayBG parameters
    BW: float = BW_DEFAULT
    Ib: float = BASAL_DEFAULT  # basal [mU/kg/min]
    CR_true: float = CR_NOMINAL  # matched carb ratio [g/U]
    dose_mult: float = 1.0     # m: programmed CR = CR_true * m  (carb error)

    @property
    def safe_name(self) -> str:
        return re.sub(r"[^A-Za-z0-9._+-]", "_", self.name)

    @property
    def cr_prog(self) -> float:
        """Programmed (mis-set) carb ratio actually used to dose."""
        return self.CR_true * self.dose_mult


# ---------------------------------------------------------------------------
# Input construction (mU/kg/min, mg/kg/min) at sample cadence
# ---------------------------------------------------------------------------
def _build_inputs(subject: Phase0Subject, hours: float,
                  bolus_factor: float, basal_factor: float, seed: int):
    """Basal + carb-bolus inputs for one therapy, at SAMPLE_TIME cadence.

    Boluses are dosed at the *programmed* (mis-set) carb ratio ``cr_prog`` and
    then scaled by ``bolus_factor``; basal is scaled by ``basal_factor``. With
    ``bolus_factor = m`` the carb term is restored to the matched dose, so
    f* = m by construction.
    """
    n = int(round(hours * 60.0 / SAMPLE_TIME))
    bins_per_min = 1.0 / SAMPLE_TIME
    insulin = np.full(n, subject.Ib * basal_factor)
    cho = np.zeros(n)
    n_abs = max(1, int(round(ABSORB_MIN / SAMPLE_TIME)))   # sample bins per meal
    for h, grams in meal_plan(hours, seed):
        b = int(round(h * 60.0 / SAMPLE_TIME))
        if b >= n:
            continue
        # CHO: constant rate over the absorption smear [mg/kg/min]
        r_cho = (grams * 1000.0 / subject.BW) / (n_abs * SAMPLE_TIME)
        cho[b:b + n_abs] += r_cho
        # Bolus: U = grams / cr_prog, delivered over one sample bin.
        # Rate persists SAMPLE_TIME min -> mass = rate * SAMPLE_TIME = U*1000/BW.
        U = (grams / subject.cr_prog) * bolus_factor
        insulin[b] += (U * 1000.0 / subject.BW) / SAMPLE_TIME
    return insulin, cho


def _add_noise(bg: np.ndarray, seed: int, sigma: float = SIGMA) -> np.ndarray:
    return bg + np.random.default_rng(seed).normal(0.0, sigma, size=len(bg))


def simulate_run(subject: Phase0Subject, hours: float,
                 bolus_factor: float = 1.0, basal_factor: float = 1.0,
                 seed: int = SEED, sigma: float = SIGMA) -> RBGRun:
    """Run one therapy through the ReplayBG plant -> an ``RBGRun``."""
    insulin_s, cho_s = _build_inputs(subject, hours, bolus_factor, basal_factor, seed)
    reps = int(round(SAMPLE_TIME / DT))
    insulin_dt = np.repeat(insulin_s, reps)
    cho_dt = np.repeat(cho_s, reps)
    Ib = float(insulin_dt[0])
    _, ig = simulate(subject.theta, insulin_dt, cho_dt, Ib=Ib, dt=DT)
    obs_idx = sample_indices(len(insulin_s), SAMPLE_TIME, DT)
    bg = ig[obs_idx]
    cgm = _add_noise(bg, seed=seed, sigma=sigma)
    return RBGRun(_cgm=cgm, _bg=bg, _insulin_sample=insulin_s, _cho_sample=cho_s,
                  sample_time=SAMPLE_TIME, BW=subject.BW, sensor_name=SENSOR,
                  meta={"hours": hours, "bolus_factor": bolus_factor,
                        "basal_factor": basal_factor, "truncated": False})


def identification_run(subject: Phase0Subject, hours: float,
                       seed: int = SEED) -> RBGRun:
    """Baseline (mis-dosed) run every twin identifies from: bolus_x1.00."""
    return simulate_run(subject, hours, bolus_factor=1.0, basal_factor=1.0, seed=seed)


def ground_truth(subject: Phase0Subject, hours: float,
                 bolus_factors, basal_factors=(), seed: int = SEED):
    """Candidate-grid ground-truth runs -> ``{policy_name: RBGRun}``.

    Policy names match ``exp_common.seen_unseen`` / ``therapy_controllers``
    (``bolus_x{f:.2f}`` / ``basal_x{f:.2f}``) so the scoring code is identical.
    """
    runs = {}
    for f in bolus_factors:
        runs[f"bolus_x{f:.2f}"] = simulate_run(subject, hours, bolus_factor=f,
                                               basal_factor=1.0, seed=seed)
    for f in basal_factors:
        runs[f"basal_x{f:.2f}"] = simulate_run(subject, hours, bolus_factor=1.0,
                                               basal_factor=f, seed=seed)
    return runs


# ---------------------------------------------------------------------------
# Per-patient CR calibration: make the matched (no-error) dose near-optimal
# ---------------------------------------------------------------------------
def calibrate_cr_true(theta, BW=BW_DEFAULT, Ib=BASAL_DEFAULT, hours=WINDOW_HOURS,
                      seed=SEED, n_grid: int = 36, d_max: float = 9.0) -> float:
    """Pick ``CR_true`` so a *carb-error-free* patient's optimal dose is f=1.

    We dose a reference patient (no carb error, m=1) at ``CR_NOMINAL`` and sweep
    a fine dose multiplier ``d``; the risk-minimizing ``d*`` is the dose the
    patient actually needs, so the matched carb ratio is ``CR_NOMINAL / d*``.
    Applying the carb error ``m`` on top then yields f* = m on the real grid.

    The sweep runs out to ``d_max`` so ``d*`` stays interior even for the most
    insulin-resistant draws; if it ever pins the ceiling, that patient is
    flagged (raise ``d_max`` or tighten ``draw_theta``'s SI floor). The absolute
    dose magnitude is irrelevant to the benchmark -- only that f* = m is bracketed
    by the candidate grid.
    """
    ref = Phase0Subject(name="_calib", theta=theta_to_array(theta), BW=BW, Ib=Ib,
                        CR_true=CR_NOMINAL, dose_mult=1.0)
    ds = np.linspace(0.3, d_max, n_grid)
    rewards = []
    for d in ds:
        run = simulate_run(ref, hours, bolus_factor=float(d), basal_factor=1.0,
                           seed=seed, sigma=0.0)   # noise-free for calibration
        rewards.append(value.reward(run.bg()))
    j = int(np.argmax(rewards))
    if j == len(ds) - 1:
        import warnings
        warnings.warn("calibrate_cr_true: d* pinned the ceiling; raise d_max "
                      "or tighten the SI draw", RuntimeWarning)
    return float(CR_NOMINAL / float(ds[j]))


# ---------------------------------------------------------------------------
# Cohort: deterministic theta draws + carb error  (auditable, reproducible)
# ---------------------------------------------------------------------------
def _hash_rng(name: str, salt: str = "") -> np.random.Generator:
    h = hashlib.sha256(f"{salt}:{name}".encode("utf-8")).hexdigest()
    return np.random.default_rng(int(h[:16], 16))


def carb_error_multiplier(name: str, choices=DEFAULT_CARB_ERROR_CHOICES) -> float:
    """Deterministic per-patient dose multiplier m (same scheme as patients.py)."""
    h = int(hashlib.sha256(str(name).encode("utf-8")).hexdigest(), 16)
    return float(choices[h % len(choices)])


def draw_theta(name: str) -> np.ndarray:
    """Deterministic true theta for a patient, log-uniform-ish within the prior.

    Centered on the installed population centre (``get_pop_theta()``), with a
    per-parameter multiplicative spread for the seven rate constants and an
    additive spread for Gb, all clipped strictly inside the prior box so
    identification is well-posed.

    Phase-0 entry points install :data:`PHASE0_CENTER` (the published prior
    centre) before calling this; if nothing is installed, ``get_pop_theta``
    raises rather than silently using a default.
    """
    rng = _hash_rng(name, salt="theta")
    base = theta_to_array(get_pop_theta()).copy()
    theta = base.copy()
    # rates: lognormal spread (~+/-40%), Gb: +/-25 mg/dL
    n = _priors.N_LOG_RATES
    theta[:n] = base[:n] * np.exp(rng.normal(0.0, 0.35, size=n))
    theta[n] = base[n] + rng.uniform(-25.0, 25.0)
    return np.clip(theta, PRIOR_LO * 1.02, PRIOR_HI * 0.98)


def make_cohort(n: int, prefix: str = "rbg", seed: int = SEED, calibrate: bool = True,
                hours: float = WINDOW_HOURS, verbose: bool = True):
    """Build ``n`` Phase 0 subjects with calibrated CR_true and carb errors."""
    subs = []
    for i in range(n):
        name = f"{prefix}{i + 1:04d}"
        theta = draw_theta(name)
        cr = calibrate_cr_true(theta, hours=hours, seed=seed) if calibrate else CR_NOMINAL
        m = carb_error_multiplier(name)
        subs.append(Phase0Subject(name=name, theta=theta, BW=BW_DEFAULT,
                                  Ib=BASAL_DEFAULT, CR_true=cr, dose_mult=m))
        if verbose and (i + 1) % 50 == 0:
            print(f"[phase0] calibrated {i + 1}/{n}")
    return subs


